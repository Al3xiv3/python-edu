a = {0, 1, 2, 4, 5}
b = {5, 2, 6, 8, 9}

print(a | b) # элементы входящие в оба множества (без повтора)
print(a & b) # элементы повторяющиеся в обоих множествах
print(a - b) # убирает элементы множества а из множества b
print(a ^ b) # убирает повторяющиеся в обоих множествах элементы и выводит общие (неповторяющиеся)
